public class Bean4 {


    private Integer a;

    private Long b;

    private Double c;

    private Character d;

    private Float e;

    private Boolean f;

    private String g;

    private Short h;

    private Byte i;


    public Integer getA() {
        return a;
    }

    public void setA(Integer a) {
        this.a = a;
    }

    public Long getB() {
        return b;
    }

    public void setB(Long b) {
        this.b = b;
    }

    public Double getC() {
        return c;
    }

    public void setC(Double c) {
        this.c = c;
    }

    public Character getD() {
        return d;
    }

    public void setD(Character d) {
        this.d = d;
    }

    public Float getE() {
        return e;
    }

    public void setE(Float e) {
        this.e = e;
    }

    public Boolean getF() {
        return f;
    }

    public void setF(Boolean f) {
        this.f = f;
    }

    public String getG() {
        return g;
    }

    public void setG(String g) {
        this.g = g;
    }

    public Short getH() {
        return h;
    }

    public void setH(Short h) {
        this.h = h;
    }

    public Byte getI() {
        return i;
    }

    public void setI(Byte i) {
        this.i = i;
    }
}
