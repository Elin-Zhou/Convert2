public class Bean1 {

    private int a;

    private long b;

    private double c;

    private char d;

    private float e;

    private boolean f;

    private String g;

    private short h;

    private byte i;


    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public long getB() {
        return b;
    }

    public void setB(long b) {
        this.b = b;
    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }

    public char getD() {
        return d;
    }

    public void setD(char d) {
        this.d = d;
    }

    public float getE() {
        return e;
    }

    public void setE(float e) {
        this.e = e;
    }

    public boolean isF() {
        return f;
    }

    public void setF(boolean f) {
        this.f = f;
    }

    public String getG() {
        return g;
    }

    public void setG(String g) {
        this.g = g;
    }

    public short getH() {
        return h;
    }

    public void setH(short h) {
        this.h = h;
    }

    public byte getI() {
        return i;
    }

    public void setI(byte i) {
        this.i = i;
    }
}
